Insert into personas (nom_per, apelpat_per, apelmat_per, fechanac_per)
			Values (@nom_per,@apelpat_per,@apelmat_per,@fechanac_per);			
insert into contactos (id_relcontper, tel_cont, calle_cont, num_cont, codpost_cont, correo_cont)
			values ((select id_per from personas where nom_per = @nom_per),@tel_cont, @calle_cont, @num_cont, @codpost_cont, @correo_cont);

-- //////////// Insert para usuarios
Insert into empleados (id_em, usuario_em, password_em, salario_em)
			Values ((select id_per from personas where (nom_per = @nom_per) and (fechanac_per = @fechanac_per)),@usuario_em,@password_em,@salario_em);

-- //////////// Insert para clientes
